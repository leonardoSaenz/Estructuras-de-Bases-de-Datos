    ax1.set_ylim(0, 10000)
    ax1.scatter(xs, ys, color='gray', s=5, zorder=3, label='puntos de entrega')
    if resultadosRadio:
        xr = [p[0] for p in resultadosRadio]
        yr = [p[1] for p in resultadosRadio]
        ax1.scatter(xr, yr, color='green', s=30, zorder=4,
                    label=f'dentro del radio ({len(resultadosRadio)})')
    circulo = plt.Circle(puntoBusqueda, radio, color='blue', fill=False,
                         linewidth=1.5, linestyle='--', zorder=5)
    ax1.add_patch(circulo)
