    ax1.legend()
    ax1.set_xlabel("X (metros)")
    ax1.set_ylabel("Y (metros)")
    ax1.grid(True, alpha=0.3)


    ax2 = axes[1]
    ax2.set_title("Vecino más cercano")
    ax2.set_xlim(0, 10000)
    ax2.set_ylim(0, 10000)
    ax2.scatter(xs, ys, color='gray', s=5, zorder=3, label='puntos de entrega')
